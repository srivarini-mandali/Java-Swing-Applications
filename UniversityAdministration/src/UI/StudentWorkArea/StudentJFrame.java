    public void populateTable()
    {
       Student s = this.business.getStudentDirectory().findByUserName(this.userAccount.getUsername());
    
      tableModel.setRowCount(0);
      
        
      for(StudentCourseLoad scl: s.getStudentCourseLoadDirectory().getCourseLoadList())
      { 
         Object row[] = new Object[6] ;
              row[0] = scl.getCourseOffer().getCourse().getId() ;
              row[1] = scl.getCourseOffer().getCourse().getName();
              row[2] = scl.getCourseOffer().getProfessor().getId();
              row[3] = scl.getCourseOffer().getProfessor().getName();
              row[4] = scl.getIsFeePaid();
              row[5] = scl.getGrade();
              
                      
           
              
              
              tableModel.addRow(row);
       }
      
    }

    public void populateDetails(){
        String username = this.userAccount.getUsername();
        Student student = this.business.getStudentDirectory().findByUserName(username);
        txtName.setText(student.getFirstName() + student.getLastName());
        txtId.setText(String.valueOf(student.getId()));
        txtEmail.setText(student.getEmailId());
        txtCurrentGPA.setText(String.valueOf(student.getGpa()));
        txtDegreeName.setText(student.getDegree().getName());
        String degreeRequirement = "Credits: " + String.valueOf(student.getDegree().getCreditRequirement()) + ", GPA: " + student.getDegree().getGpaRequirement();
        txtDegreeRequirement.setText(degreeRequirement);
        txtDegreeEarned.setText(student.getDegreeEarned());
        txtTotalCredits.setText(String.valueOf(student.getTotalCreditsCompleted()));
    }

    
