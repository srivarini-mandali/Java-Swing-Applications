    private void populateComboBoxes() {
       ArrayList<StudentCourseLoad> courses = currentStudent.getStudentCourseLoadDirectory().getCourseLoadList(); 

    for(StudentCourseLoad course : courses) {
        Course addcourse = course.getCourseOffer().getCourse();
        comboBoxCourse.addItem(addcourse.getName()); 
        
        // Get the professor for the current course
        Professor professor = course.getCourseOffer().getProfessor();
        
        if(professor != null) {
            comboProfessor.addItem(professor.getName()); 
        }
    }
    }

    private Professor findProfessorByName(String professorName) {
        List<Professor> professorList = business.getProfessorDirectory().getProfessorList();

    for (Professor professor : professorList) {
        if (professor.getName().equals(professorName)) {
            return professor; // Return the professor with the matching name
        }
    }

    return null;
        
    }

    
    
