    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
    String feedback = jTextArea1.getText();
    String courseName = (String) comboBoxCourse.getSelectedItem();
    String professorName = (String) comboProfessor.getSelectedItem();
    Professor professor = findProfessorByName(professorName);
if (professor != null) {
        // Create a Feedback object and add it to the professor's feedback list
        Feedback feedbackObject = new Feedback(courseName, professorName, feedback);
        professor.addFeedback(feedbackObject);
        
        // TODO: Store the feedback, maybe in a database or a file.
        // Display a confirmation message to the user:
        JOptionPane.showMessageDialog(this, "Feedback for " + courseName + " by " + professorName + " submitted successfully!");
    } else {
        // Display an error message if the professor was not found
        JOptionPane.showMessageDialog(this, "Professor " + professorName + " not found.");
    }
        
    }//GEN-LAST:event_btnSaveActionPerformed

