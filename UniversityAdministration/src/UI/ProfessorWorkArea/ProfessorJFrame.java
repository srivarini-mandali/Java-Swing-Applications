    public void populate(){
        
       
    Professor p = this.business.getProfessorDirectory().findByUserName(useraccount.getUsername());

    if (p != null) {
        // Professor object is not null, you can access its properties safely
        txtId.setText(p.getId());
        txtName.setText(p.getName());
        txtReputationIndex.setText(String.valueOf(p.getReputationIndex()));
        txtEmail.setText(p.getEmail());
        txtSubscriptionFee.setText(String.valueOf(p.isFeesPaid()));
        
        professorCourseTable.setRowCount(0);

        for (CourseOffer b : p.getProfessorCourseOfferDir().getCourseOfferList()) {
            Object row[] = new Object[5];
            row[0] = b;
            row[1] = b.getPrice();
            row[2] = b.getCourse().getDuration();
            row[3] = b.getCourse().getCredits();
            row[4] = b.getTime();

            professorCourseTable.addRow(row);
        }
    } else {
        // Handle the case when the Professor object is null
        // For example, you can display an error message or clear the fields.
        txtName.setText("");  // Clear the name field
        txtReputationIndex.setText("");  // Clear the reputation index field
        txtEmail.setText("");  // Clear the email field
        txtSubscriptionFee.setText("");  // Clear the subscription fee field
        professorCourseTable.setRowCount(0);  // Clear the course table
        // You can also display an error message, e.g., JOptionPane.showMessageDialog.
        // JOptionPane.showMessageDialog(this, "Professor not found", "Error", JOptionPane.ERROR_MESSAGE);
    }


    
    }
