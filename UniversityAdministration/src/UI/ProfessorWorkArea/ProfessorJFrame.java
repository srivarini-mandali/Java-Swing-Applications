    private void btnUpdateCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateCourseActionPerformed
        // TODO add your handling code here:
        int selectedRow = profCourseTable.getSelectedRow();

    if (selectedRow == -1) {
        // No course is selected, show an error message or prompt the user to select a course
        JOptionPane.showMessageDialog(this, "Please select a course to update.", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        // Get the selected course from the table
        CourseOffer selectedCourse = (CourseOffer) profCourseTable.getValueAt(selectedRow, 0);
        Professor p = this.business.getProfessorDirectory().findByUserName(useraccount.getUsername());
        new UpdateCourseJFrame(this.business,this.useraccount,p,selectedCourse);
        this.setVisible(false);

    }
    
    }//GEN-LAST:event_btnUpdateCourseActionPerformed

    private void btnDeleteCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCourseActionPerformed
        // TODO add your handling code here:
          int selectedRow = profCourseTable.getSelectedRow();

    if (selectedRow == -1) {
        // No course is selected, show an error message or prompt the user to select a course
        JOptionPane.showMessageDialog(this, "Please select a course to delete.", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        // Get the selected course from the table
        CourseOffer selectedCourse = (CourseOffer) profCourseTable.getValueAt(selectedRow, 0);
        Professor p = this.business.getProfessorDirectory().findByUserName(useraccount.getUsername());
        p.getProfessorCourseOfferDir().getCourseOfferList().remove(selectedCourse);
        professorCourseTable.removeRow(selectedRow);
        JOptionPane.showMessageDialog(this, "Course deleted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
    }

        
    }//GEN-LAST:event_btnDeleteCourseActionPerformed

