    public void viewDetails()
    { 
      id_txt.setText(p.getId());
      firstName_txt.setText(p.getFirstName());
      lastName_txt.setText(p.getLastName());
      email_txt.setText(p.getEmail());
      
       tableModel.setRowCount(0);
        
      for(CourseOffer b: p.getProfessorCourseOfferDir().getCourseOfferList())
      { 
        Object row[] = new Object[4] ;
              row[0] = b.getCourse().getId() ;
              row[1] = b.getPrice();
              row[2] = b.getCourse().getDuration();
              row[3] = b.getCourse().getCredits();
     
              
              
              tableModel.addRow(row);
         
      }
      
    }
    
