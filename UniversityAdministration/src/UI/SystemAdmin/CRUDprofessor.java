    public void populateTable()
    {
       tableModel.setRowCount(0);
        
      for(Professor b: this.business.getProfessorDirectory().getProfessorList())
      { 
        Object row[] = new Object[4] ;
              row[0] = b ;
              row[1] = b.getFirstName();
              row[2] = b.getLastName();
              row[3] = b.getEmail();              
              tableModel.addRow(row);
         
      }
    
    }
    
