    public void populateTable()
    {
      tableModel.setRowCount(0);
        
      for(Student b: this.business.getStudentDirectory().getStudentList())
      { 
        Object row[] = new Object[4] ;
              row[0] = b ;
              row[1] = b.getFirstName();
              row[2] = b.getLastName();
              row[3] = b.getEmailId();
     
              
              
              tableModel.addRow(row);
         
      }
    
    }
    
