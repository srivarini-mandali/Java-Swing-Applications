    private Degree getDegreeByString(String degreeString) {
        for (Degree degree : this.business.getDegreeDirectory().getDegreeList()) {
        System.out.println("Comparing: \"" + degreeString + "\" with \"" + degree.toString() + "\"");
        if (degree.toString().trim().equals(degreeString.trim())) {
            return degree;
        }
    }
    return null;
    }
    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
       int selectedRow = jTable2.getSelectedRow();

    if (selectedRow == -1) {
        // No row is selected, show an error message or prompt the user to select a degree
        JOptionPane.showMessageDialog(this, "Please select a degree to delete.", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        String degreeName = (String) jTable2.getValueAt(selectedRow, 0);
        Degree d = this.business.getDegreeDirectory().findByName(degreeName);
        if (d != null) {
            // If the degree exists, remove it from the directory
            this.business.getDegreeDirectory().getDegreeList().remove(d);
            populateTable(); // Refresh the table to reflect the changes
            JOptionPane.showMessageDialog(this, "Degree removed successfully!");
        } else {
            // If the degree is not found in the directory (should not happen if the table is in sync)
            JOptionPane.showMessageDialog(this, "Error: Degree not found in the directory.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_jButton10ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String degreeName = fieldName.getText();
        int creditRequirement = Integer.valueOf(fieldCreditRequirement.getText());
        float gpaRequirement = Float.valueOf(fieldGpaRequirement.getText());
         if (isDuplicate(degreeName)) {
        JOptionPane.showMessageDialog(this, "This degree already exists. Please enter a unique degree name.", "Error", JOptionPane.ERROR_MESSAGE);
         } else {
        this.business.getDegreeDirectory().addDegree(degreeName, creditRequirement, gpaRequirement);
        populateTable();
        JOptionPane.showMessageDialog(null, "Degree successfully added!");
        fieldName.setText("");
        fieldCreditRequirement.setText("");
        fieldGpaRequirement.setText("");
         }
    }//GEN-LAST:event_jButton7ActionPerformed
    private boolean isDuplicate(String degreeName) {
        for (Degree d : this.business.getDegreeDirectory().getDegreeList()) {
            if (d.getName().equalsIgnoreCase(degreeName)) {
                return true; // Found a duplicate
            }
        }
        return false; // No duplicate found
    }
