    private void populateTable(){
        tableModel.setRowCount(0);
        for(Degree d: this.business.getDegreeDirectory().getDegreeList()){
            Object[] row = new Object[2];
            row[0] = d.getName();
            row[1] = "Credits: " + String.valueOf(d.getCreditRequirement()) + ", GPA: " + d.getGpaRequirement();
 
            tableModel.addRow(row);
        }
    }


