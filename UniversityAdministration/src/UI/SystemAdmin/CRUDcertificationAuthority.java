    public void populateTable()
    {
       tableModel1.setRowCount(0);
        
      for(CertificationAuthority b: this.business.getCertificationAuthorityDirectory().getList())
      { 
        Object row[] = new Object[1] ;
              row[0] = b ;
              tableModel1.addRow(row);
         
      }
    
    }
    
