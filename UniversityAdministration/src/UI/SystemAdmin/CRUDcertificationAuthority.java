    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
          int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        // No row is selected, show an error message or prompt the user to select an authority
        JOptionPane.showMessageDialog(this, "Please select an authority to update.", "Error", JOptionPane.ERROR_MESSAGE);
        
    } else {
        CertificationAuthority c = (CertificationAuthority) tableModel1.getValueAt(selectedRow, 0);
        new UpdateCertificationAuthority(this.business, this.useraccount, c);
        this.setVisible(false);
    }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        // No row is selected, show an error message or prompt the user to select an authority
        JOptionPane.showMessageDialog(this, "Please select an authority to delete.", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        CertificationAuthority c = (CertificationAuthority) tableModel1.getValueAt(selectedRow, 0);
        this.business.getCertificationAuthorityDirectory().getList().remove(c);
        populateTable();
    }
    }//GEN-LAST:event_jButton5ActionPerformed

