   public void populateTable() {

    // Clear the existing data in the table
    tableModel.setRowCount(0);

    // Replace 'getDegreeApprovalRequests' with the actual method to get degree approval requests
    List<DegreeRequest> degreeApprovalRequests = business.getCertificationAuthorityDirectory().getDegreeApprovalRequests();

    for (DegreeRequest request : degreeApprovalRequests) {
        Object[] row = new Object[6]; // Assuming you want to display various details

        // Populate the row with data from the DegreeRequest
        row[0] = request.getStudent().getId();
        row[1] = request.getDegree().getName();
        row[2] = request.getDegree().getCreditRequirement();
        row[3] = request.getStudent().getTotalCreditsCompleted();
        row[4] = request.getStudent().getGpa();
        row[5] = request.getStatus();

        tableModel.addRow(row);
    }
}

    
