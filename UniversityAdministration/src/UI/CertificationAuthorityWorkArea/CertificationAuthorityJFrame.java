    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCertificateAuthority.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select student to Approve  ","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
         DegreeRequest c = (DegreeRequest) tableModel.getValueAt(selectedRow, 0) ;
         c.setStatus("Approved");
         populateTable() ;
        }
        
    }//GEN-LAST:event_btnApproveActionPerformed

