    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String supplier_name = supplierNameText.getText();
        String product_name = productNameText.getText();
        int floor_price = Integer.parseInt(fpText.getText());
        int ceiling_price = Integer.parseInt(cpText.getText());
        int target_price = Integer.parseInt(tpText.getText());
        
        Supplier supplier = business.getSupplierDirectory().findSupplier(supplier_name);
        
        Product p = supplier.getProductCatalog().getProduct(product_name);
        p.updateProduct(floor_price, ceiling_price, target_price);
        JOptionPane.showMessageDialog(null, "Prices successfully updated");
        initializeTable();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRowIndex = managePriceTbl.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to view.");
        }
        else{
            DefaultTableModel model = (DefaultTableModel) managePriceTbl.getModel();
            supplierNameText.setText(String.valueOf(model.getValueAt(selectedRowIndex, 0)));
            productNameText.setText(String.valueOf(model.getValueAt(selectedRowIndex, 1)));
            fpText.setText(String.valueOf(model.getValueAt(selectedRowIndex, 2)));
            cpText.setText(String.valueOf(model.getValueAt(selectedRowIndex, 3)));
            tpText.setText(String.valueOf(model.getValueAt(selectedRowIndex, 4)));
        }
    }//GEN-LAST:event_jButton2ActionPerformed


