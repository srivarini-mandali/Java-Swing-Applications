    private void refreshTable() {
        int rc = salesPersonOrdersTbl.getRowCount();
        int i;
        for (i = rc - 1; i >= 0; i--) {
            ((DefaultTableModel) salesPersonOrdersTbl.getModel()).removeRow(i);
        }

        String salesprsonname = (String) salesPersonCombo.getSelectedItem();
        ArrayList<Order> orderlist = business.getMasterorderlist().getOrders();

        if (orderlist.isEmpty()) {
            return;
        }
        
        for (Order o : orderlist) {
            for(OrderItem oi: o.getOrderitems()){
                if(o.getSalesperson().getSalesPersonId().equals(salesprsonname)){
                    Object[] row = new Object[6];
                    row[0] = o.getOrder_id();
                    row[1] = oi.getSelectedProduct().getName();
                    row[2] = oi.getActualPrice();
                    row[3] = oi.getQuantity();
                    row[4] = oi.calculatePricePerformance();
                    row[5] = o.getCommission();
                    ((DefaultTableModel) salesPersonOrdersTbl.getModel()).addRow(row);
                }   
            }   
        } 
    }

    private void initializeTable() {
        ArrayList<Order> orderlist = business.getMasterorderlist().getOrders(); 
        String salespersonname = (String) salesPersonCombo.getSelectedItem();
        
        for (Order o : orderlist) {
            for(OrderItem oi: o.getOrderitems()){
                if(o.getSalesperson().getSalesPersonId().equals(salespersonname)){
                    Object[] row = new Object[6];
                    row[0] = o.getOrder_id();
                    row[1] = oi.getSelectedProduct().getName();
                    row[2] = oi.getActualPrice();
                    row[3] = oi.getQuantity();
                    row[4] = oi.calculatePricePerformance();
                    row[5] = o.getCommission();
                    ((DefaultTableModel) salesPersonOrdersTbl.getModel()).addRow(row);
                }
            }
            
        }
    }

    private boolean isItemInComboBox(JComboBox<String> customerCombo, String item) {
        ComboBoxModel<?> model = customerCombo.getModel();
        for (int i = 0; i < model.getSize(); i++) {
            if (model.getElementAt(i).equals(item)) {
                return true;
            }
        }
        return false;
    }

    private void populateCombo() {
        salesPersonCombo.removeAllItems();

        int rc = salesPersonOrdersTbl.getRowCount();
        int i;
        for (i = rc - 1; i >= 0; i--) {
            ((DefaultTableModel) salesPersonOrdersTbl.getModel()).removeRow(i);
        }

        ArrayList<Order> orderlist = business.getMasterorderlist().getOrders();
        for (Order o : orderlist) {
            // if customer not in combo box, add customer
            if (!isItemInComboBox(salesPersonCombo, o.getSalesperson().getSalesPersonId())) {
                salesPersonCombo.addItem(o.getSalesperson().getSalesPersonId());
            }         
        }
        salesPersonCombo.setSelectedIndex(0);
    }


