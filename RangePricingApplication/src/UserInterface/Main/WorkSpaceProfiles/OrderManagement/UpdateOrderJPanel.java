public void setOrderDetails(Order order) {
    // Set the fields in the UpdateOrderJPanel using the details from the selected order
     jTextField1.setText(String.valueOf(order.getModelNumber()));

        // Format and set Issue Date
        jTextField2.setText(formatDate(order.getIssueDate()));

        // Format and set Shipping Date
        jTextField3.setText(formatDate(order.getShippingDate()));

        // Format and set Completion Date
        jTextField4.setText(formatDate(order.getCompletionDate()));

        // Set Order Status
        jTextField5.setText(order.getStatus());
     }
     private String formatDate(LocalDateTime dateTime) {
        if (dateTime == null) {
            return "";
        }
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        return dateTime.format(formatter);
    }

