    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
       try {
        // Assuming your Order class has appropriate setter methods.
        // Update model number - considering it's not editable, no need to update it here.
        // order.setModelNumber(Integer.parseInt(jTextField1.getText()));

        // Update issue date
        if (!jTextField2.getText().isEmpty()) {
            LocalDateTime issueDate = LocalDateTime.parse(jTextField2.getText(), DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss"));
            order.setIssueDate(issueDate);
        }

        // Update shipping date
        if (!jTextField3.getText().isEmpty()) {
            LocalDateTime shippingDate = LocalDateTime.parse(jTextField3.getText(), DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss"));
            order.setShippingDate(shippingDate);
        }

        // Update completion date
        if (!jTextField4.getText().isEmpty()) {
            LocalDateTime completionDate = LocalDateTime.parse(jTextField4.getText(), DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss"));
            order.setCompletionDate(completionDate);
        }

        // Update status
        order.setStatus(jTextField5.getText());

        // Notify CheckOrderStatus to refresh the table
        onUpdateCallback.run();

        // Optionally, show a confirmation message
        JOptionPane.showMessageDialog(this, "Order updated successfully.", "Update Successful", JOptionPane.INFORMATION_MESSAGE);
    } catch (DateTimeParseException e) {
        JOptionPane.showMessageDialog(this, "Invalid date format. Please use 'yyyy-MM-dd HH:mm:ss'.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid number format.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error updating order: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

        
    }//GEN-LAST:event_btnUpdateActionPerformed

