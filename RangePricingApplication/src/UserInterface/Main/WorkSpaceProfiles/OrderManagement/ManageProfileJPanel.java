    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String newUsername = txtUsername.getText();
       String newPassword = txtPassword.getText();

    // Assuming you have access to the current logged-in salesperson's profile
      UserAccountDirectory userAccountDirectory = business.getUserAccountDirectory();

    // Iterate over the user accounts to find the one associated with the current salesperson
    for (UserAccount ua : userAccountDirectory.useraccountlist) {
        if (ua.getAssociatedPersonProfile().equals(salesperson)) {
            // Update the username and password in the UserAccount
            ua.setUsername(newUsername);
            ua.setPassword(newPassword);

            // Display a confirmation message
            JOptionPane.showMessageDialog(this, "Profile updated successfully.", "Update Successful", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
    }

    // Display an error message if the user account is not found
    JOptionPane.showMessageDialog(this, "User account not found.", "Error", JOptionPane.ERROR_MESSAGE);

    }//GEN-LAST:event_btnUpdateActionPerformed


